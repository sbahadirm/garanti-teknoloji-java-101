package org.bahadirmemis.java101;

/**
 * @author bahadirmemis
 */
public class ForLoop {

  public static void main(String[] args) {

    for (int i = 0; i < 5; i++){
      System.out.println(i);
    }

    /** for
     * (
     * part1
     * ;
     * part2
     * ;
     * part3
     * )
     *
     * */
    int i = 0;
    for (; i<5; i++){
      System.out.println(i);
    }
  }
}
